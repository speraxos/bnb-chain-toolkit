// src/tools/binance-futures-coinm/account-api/balance.ts
import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { deliveryClient } from "../../../config/binanceClient.js";
import { z } from "zod";

export function registerBinanceDeliveryBalance(server: McpServer) {
    server.tool(
        "BinanceDeliveryBalance",
        "Get current COIN-M Futures account balance.",
        {
            recvWindow: z.number().int().optional().describe("Recv window in milliseconds")
        },
        async (params) => {
            try {
                const response = await deliveryClient.restAPI.balance({
                    ...(params.recvWindow && { recvWindow: params.recvWindow })
                });
                
                const data = await response.data();
                
                return {
                    content: [{
                        type: "text",
                        text: `üí∞ COIN-M Futures Balance\n\n${JSON.stringify(data, null, 2)}`
                    }]
                };
            } catch (error) {
                const errorMessage = error instanceof Error ? error.message : String(error);
                return {
                    content: [{ type: "text", text: `‚ùå Failed to get balance: ${errorMessage}` }],
                    isError: true
                };
            }
        }
    );
}
