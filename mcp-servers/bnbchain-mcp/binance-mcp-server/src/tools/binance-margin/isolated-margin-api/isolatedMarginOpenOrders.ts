// src/tools/binance-margin/isolated-margin-api/isolatedMarginOpenOrders.ts
import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { marginClient } from "../../../config/binanceClient.js";
import { z } from "zod";

export function registerBinanceIsolatedMarginOpenOrders(server: McpServer) {
    server.tool(
        "BinanceIsolatedMarginOpenOrders",
        "Query current open orders in isolated margin account.",
        {
            symbol: z.string().optional().describe("Symbol of the trading pair (optional)"),
            recvWindow: z.number().int().optional().describe("Time window for request validity")
        },
        async (params) => {
            try {
                const response = await marginClient.restAPI.queryMarginAccountsOpenOrders({
                    isIsolated: "TRUE",
                    ...(params.symbol && { symbol: params.symbol }),
                    ...(params.recvWindow && { recvWindow: params.recvWindow })
                });

                const data = await response.data();
                return {
                    content: [{
                        type: "text",
                        text: `Isolated Margin Open Orders: ${JSON.stringify(data, null, 2)}`
                    }]
                };
            } catch (error) {
                const errorMessage = error instanceof Error ? error.message : String(error);
                return {
                    content: [{ type: "text", text: `Failed to query open orders: ${errorMessage}` }],
                    isError: true
                };
            }
        }
    );
}
